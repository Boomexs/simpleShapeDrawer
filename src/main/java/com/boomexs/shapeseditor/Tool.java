package com.boomexs.shapeseditor;

/**
 * The {@code Tool} enum represents the different tools available in the shapes editor.
 * These tools include EDIT, RECTANGLE, CIRCLE, and TRIANGLE.
 */
public enum Tool {
    /**
     * The EDIT tool, used for editing existing shapes.
     */
    EDIT,
    /**
     * The RECTANGLE tool, used for creating rectangles.
     */
    RECTANGLE,
    /**
     * The CIRCLE tool, used for creating circles.
     */
    CIRCLE,
    /**
     * The TRIANGLE tool, used for creating triangles.
     */
    TRIANGLE
}
